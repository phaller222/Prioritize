package de.hallerweb.enterprise.prioritize.model;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Version;
import javax.validation.constraints.Size;

/**
 * Address.java - JPA class to represent an {@link Address}.
 * 
 * <p>
 * Copyright: (c) 2014
 * </p>
 * <p>
 * Peter Haller
 * </p>
 * 
 * @author peter
 */
@Entity
@NamedQueries(@NamedQuery(name = "findAllAddresses", query = "SELECT a FROM Address a ORDER BY a.id"))
public class Address {

	@Id
	@GeneratedValue
	int id;

	@Size(min = 5, max = 5, message = "ZIP-code must be 5 digits in length!")
	String zipCode;
	String phone;
	String fax;
	String city;
	String street;
	String email;

	@Version
	private int entityVersion; // For optimistic locks

	public int getId() {
		return id;
	}

	public String getStreet() {
		return street;
	}

	public void setStreet(String street) {
		this.street = street;
	}

	public String getZipCode() {
		return zipCode;
	}

	public void setZipCode(String zipCode) {
		this.zipCode = zipCode;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getFax() {
		return fax;
	}

	public void setFax(String fax) {
		this.fax = fax;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
}
